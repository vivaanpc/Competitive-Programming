typedef long long ll;
const int mod = 1e9 + 7;
const double eps = 1e-12;
void solve() {
    int n;
    cin >> n;
    vector<int> v(n);
    for (int i = 0; i < n; i++) {
        cin >> v[i];
    }
    sort(v.begin(), v.end());
    ll currentSum = 1;
    for (int i = 0; i < n; ++i) {
        if (currentSum < v[i]) {
            break;
        } else {
            currentSum += v[i];
        }
    }
    cout << currentSum;
}
